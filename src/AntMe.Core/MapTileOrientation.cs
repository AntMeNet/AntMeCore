namespace AntMe
{
    /// <summary>
    /// List of possible Map Tile Orientations.
    /// </summary>
    public enum MapTileOrientation
    {
        /// <summary>
        /// Tile is not rotated.
        /// </summary>
        NotRotated = 0,

        /// <summary>
        /// Tile is rotated by 90 Degrees.
        /// </summary>
        RotBy90Degrees = 90,

        /// <summary>
        /// Tile is rotated by 180 Degrees.
        /// </summary>
        RotBy180Degrees = 180,

        /// <summary>
        /// Tile is rotated by 270 Degrees.
        /// </summary>
        RotBy270Degrees = 270
    }
}
